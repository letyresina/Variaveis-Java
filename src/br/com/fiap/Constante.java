package br.com.fiap;

public class Constante {
	public static void main(String[] args) {
		
		// constantes em Java
		// convenção: toda maiúscula e com underscore (espaço)
		// usar o modificador 'final'
		
		// final + tipo + nome = valor;
		final double PI = 3.1416;
		final float GRAVIDADETERRESTRE = 9.8f;
		System.out.println("Constante PI: " + PI);
		System.out.println("A gravidade terrestre é de " + GRAVIDADETERRESTRE);
	}
}